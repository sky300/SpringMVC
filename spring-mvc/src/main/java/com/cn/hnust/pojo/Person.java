package com.cn.hnust.pojo;

public class Person {
   public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getSex() {
		return Sex;
	}
	public void setSex(String sex) {
		Sex = sex;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int aget) {
		this.age = aget;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
private String Name;
   private String Sex;
   private int age;
   private int id;
}
